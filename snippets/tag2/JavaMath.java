package tag2;

public class JavaMath {
    public static void main(String[] args) {
        System.out.println(Math.max(5, 10)); // Gibt den größeren Wert zurück
        System.out.println(Math.min(5, 10)); // Gibt den kleineren Wert zurück
        System.out.println(Math.sqrt(16));   // Gibt die Quadratwurzel zurück
        System.out.println(Math.abs(-4.3)); // Gibt den absoluten Wert zurück
        System.out.println(Math.pow(2, 8)); // Gibt 2 hoch 8 zurück
        System.out.println(Math.round(4.6)); // Rundet auf die nächste Ganzzahl
        System.out.println(Math.ceil(4.2)); // Rundet auf die nächste ganze Zahl auf
        System.out.println(Math.floor(4.8)); // Rundet auf die nächste ganze Zahl ab
        System.out.println(Math.random() * 100); // Gibt eine zufällige Zahl zwischen 0.0 und 100 zurück
    }
}
