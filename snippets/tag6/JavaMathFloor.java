package tag6;

public class JavaMathFloor {
    public static void main(String[] args) {
        double zahl1 = Math.floor(3.9); // -> 3.0
        double zahl2 = Math.floor(5.1); // -> 5.0
        double zahl3 = Math.floor(-2.3); // -> -3.0

        System.out.println(zahl1);
        System.out.println(zahl2);
        System.out.println(zahl3);
    }
}
