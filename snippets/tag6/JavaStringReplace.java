package tag6;

public class JavaStringReplace {
    public static void main(String[] args) {
        String original = "Hallo Welt! Willkommen in der Welt der Programmierung.";
        String ersetzt = original.replace("Welt", "Universe");
        System.out.println("Original: " + original);
        System.out.println("Ersetzt: " + ersetzt);
    }
}
