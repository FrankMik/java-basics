package tag6;

public class JavaMathCeil {
    public static void main(String[] args) {
        // Math.ceil rundet eine Zahl immer auf die nächstgrößere ganze Zahl auf
        double zahl1 = Math.ceil(3.1); // ->4.0
        double zahl2 = Math.ceil(-7.6); // ->-7.0
        System.out.println(zahl1);
        System.out.println(zahl2);
    }
}
